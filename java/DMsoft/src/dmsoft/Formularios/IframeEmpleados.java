    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      //Validamos que no se este realizando ninguna otra operacion y que la operacion sea por DEFAULT
       if(tipoOperacion==Utilidades.DEFAULT)
       {
          //Validamos que se haya seleccionado un empleado de la tabla
           if(tblEmpleados.getSelectedRow()>=0)
           {
               //Cambiamos el tipo de operacion
               tipoOperacion=Utilidades.EDITAR;
                btnRetornar.setVisible(true);
                
               //Consultamos todos los datos del empleado a la base de datos por medio de su cedula
               //Este metodo llenara las variables de la clase empleados
               empleado.obtenerDatos(tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(),0).toString());
                
               //Accedemos a las variables por medio de los metodos get y las asignamos a los campos
                getDatos();   
           }
           else
           {
               JOptionPane.showMessageDialog(null,"Seleccione un empleado de la tabla");
           }
       }
    }//GEN-LAST:event_btnEditarActionPerformed

