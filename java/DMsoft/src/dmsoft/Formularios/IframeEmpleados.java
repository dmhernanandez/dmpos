    //Este metodo es para darle dise√±o al modelo de la tabla 
   private  void modeloTabla(){
      //Creamos un arreglo de Strings para los encabezados de nuestra tabla
    String encabezados[]= {"DNI", "Nombre", "Apellidos", "Fecha nacimiento", 
                           "Direccion", "Telefono", "Correo",
                            "Usuario", "Clave", "Estado"};
    //Creamos un modelo para nuestros arreglo para los encabezados
   modeloRoles=new DefaultTableModel(null,encabezados){
       /*Creamos un arreglo de tipos para nuestra tabla, dicho arreglo es paralelo con el nombre 
        arreglo de nombres del encabezado*/
        Class tipos[]={
                String.class,
                String.class, 
                String.class,
                Date.class,
                String.class,
                String.class,
                String.class,
                String.class,
                String.class,
                String.class};
        //Sobre escribimos el metodo getColumIndex para que devuelva la posicion de nuestro arreglo de tipos
        @Override
         public Class getColumnClass(int index){
            return tipos[index];
         }
         //Tambien sobre escribimos el metodo que permite editar las celdas de la tabla
        @Override
         public boolean isCellEditable(int rowIndex,int columnIndex){
             return false;
         }
       };
    //Agregamos nuestro modelo a la tabla
      tblEmpleados.setModel(modeloRoles);
     }
    
    
    //Este metodo se encarga del dise√±o de la tabla, es decir del formato de las celdas
    private void formatoTabla()
    {
       int dni=180,
           Nombre=150,
           apellido=150,
           fechaNacimiento=120,
           direccion=200,
           telefono=120,
           correo=170,
           user=120,
           clave=120,
           estado=130;
           
      //Creamos un arreglo con las medidas fijos predeterminados de la tabla para cada celda
      int ancho[]={dni,Nombre,apellido,fechaNacimiento,direccion,telefono,correo,user,clave,estado};
      for(int i=0;i<tblEmpleados.getColumnCount();i++)
      {
          tblEmpleados.getColumnModel().getColumn(i).setPreferredWidth(ancho[i]);
          tblEmpleados.getColumnModel().getColumn(i).setResizable(false);
      }
      tblEmpleados.setRowHeight(20);
      //Establecemos el header
      new Utilidades().setTableHeader(tblEmpleados);
    }
   

    //Este metodo se encarga de deicidir que tipo de operacion se realizara. Si es Guardar o Actualizar
   private boolean realizarOperacion(int tipo){
       boolean realizado=false;
       switch(tipo)
       {
            case Utilidades.NUEVO:
                //llenamos la variables de la clase empleados con los valores del campo de texto
                //que luego seran enviadas en el metodo guardar o actualizar
                setDatos(tipo);
                //Luego utilizamos las variables para mandar a llamar al metodo guardar
                if(empleado.guardar())
                  realizado=true;  
            break;
            case Utilidades.EDITAR:
                setDatos(tipo);
                if(empleado.actualizar())
                    realizado=true;
            break;
       }
       return realizado;
   }

   //Con este metodo obtenemos todos los valores de los campos
   private void setDatos(int vericacion)
   {
       //Especificamos que solo cuando se valla a editar guardara el valor del id del empleado
       if(vericacion==Utilidades.EDITAR)
        empleado.setIdEmpleado(Integer.parseInt(txtCodigo.getText()));
           
      empleado.setDni(txtCedula.getText());
      empleado.setpNombre(txtPrimerNombre.getText());
      empleado.setsNombre(txtSegundoNombre.getText());
      empleado.setApellidoPaterno(txtApellidoPaterno.getText());
      empleado.setApellidoMaterno(txtApellidoMaterno.getText());
      empleado.setTelefono(txtTelefono.getText());
      empleado.setDireccion(txtdireccion.getText());
      //validamos que la fecha no sea nulla 
      if(dcrFechaNacimiento.getDate()!=null)
      {
        empleado.setFechaNacimiento(Date.valueOf(formatoFecha.format(dcrFechaNacimiento.getDate())));
      }
      else
      {
          empleado.setFechaNacimiento(null);  
      }
         
      empleado.setCorreo(txtCorreo.getText());
     //Validamos que el usuario tengo usuario, de lo contrario se asigna null, para evitar conflicto en la BD
      if(txtUsuario.getText().isEmpty())
          empleado.setUsuario(null);
      else
          empleado.setUsuario(txtUsuario.getText());
      empleado.setClave(txtPassword.getText());
      empleado.setTipoUsuario((String)cbxTipoUsuario.getSelectedItem());
      empleado.setEstado((String)cbxEstado.getSelectedItem());
   }
   
   //Con este metodo asignamos los valores a las variables a los campos 
   private void  getDatos()
   {
      
       utilidades.cambiarComponente(tpneEmpleados, panelDatos, "Editar datos del empleado");
       txtCodigo.setText(String.valueOf(empleado.getIdEmpleado()));
       txtCedula.setText(empleado.getDni());
       txtPrimerNombre.setText(empleado.getpNombre());
       txtSegundoNombre.setText(empleado.getsNombre());
       txtApellidoPaterno.setText(empleado.getApallidoPaterno());
       txtApellidoMaterno.setText(empleado.getApellidoMaterno());
       txtTelefono.setText(empleado.getTelefono());
       txtdireccion.setText(empleado.getDireccion());
       dcrFechaNacimiento.setDate(empleado.getFechaNacimiento());
       txtCorreo.setText(empleado.getCorreo());
       txtUsuario.setText(empleado.getUsuario());
       txtPassword.setText(empleado.getClave());
       cbxEstado.setSelectedItem(empleado.getEstado());
       cbxTipoUsuario.setSelectedItem(empleado.getTipoUsuario());
       /*Debido a que los combobox tiene implementa el ItemListener cada vez que seleccionamos, un item
        el envento detecta el cambio y marca la varible change como true, entonces cada vez que se hace 
       una seleccion en los comboBox entonces la reseteamos porque no es un cambio que nosotros hacemos*/
       utilidades.setChanged(false);
   }
   
